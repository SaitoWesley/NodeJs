/* 1) Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, subtração,
multiplicação e divisão desses valores */

function exibeValor(num1, num2){
    let soma = num1 + num2
    let sub = num1 - num2
    let mult = num1 * num2
    let div = num1 / num2

    return `Soma: ${soma}, Sub: ${sub}, Multiplicação: ${mult}, Divisão: ${div}`
}

console.log(exibeValor(10, 10))

